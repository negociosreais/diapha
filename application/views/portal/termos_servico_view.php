        <p>As informa&ccedil;&otilde;es do usu&aacute;rio ser&atilde;o utilizadas para as seguintes finalidades:</p>
        <p>- Gerar o login no site.</p>
        <p>- Enviar newsletters e divulga&ccedil;&otilde;es de servi&ccedil;os.</p>
        <p>O Portal ARP poder&aacute; fornecer as informa&ccedil;&otilde;es pessoais&nbsp; do usu&aacute;rio por&nbsp; interven&ccedil;&atilde;o &nbsp;legal, de acordo com as Leis vigentes.</p>
