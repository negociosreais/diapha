        <p>Para ter acesso a &Aacute;rea Restrita do Portal ARP o usu&aacute;rio dever&aacute; cadastrar-se acessando o link respectivo na are de login, preenchendo o formul&aacute;rio de cadastro para pessoa f&iacute;sica, cujos campos que possuem '*' s&atilde;o de preenchimento obrigat&oacute;rio. Al&eacute;m disso, para criar o cadastro &eacute; necess&aacute;rio informar um Login e uma Senha de uso pessoal sendo de exclusiva responsabilidade do usu&aacute;rio sua salvaguarda.&nbsp;</p>
