        <p>Ap&oacute;s a finaliza&ccedil;&atilde;o do cadastro ser&aacute; enviada uma mensagem, para o e-mail digitado no campo espec&iacute;fico, com seus dados para acesso e um link para confirma&ccedil;&atilde;o do cadastro. Esse procedimento &eacute; usado para garantir que nenhuma outra pessoa se cadastre com dados duplicados.</p>
